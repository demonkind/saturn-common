package com.huifu.saturn.common.netpay;

// data must include value and type
public class DATA {
    private String type;

	private Object value;

	public DATA(String type, Object value) {
		this.type = type;
		this.value = value;
	}

	public String getType() {
		return type;
	}

	public Object getValue() {
		return value;
	}
}
